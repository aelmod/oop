package com.github.aelmod.oop.polymorphism.source;

public class Data {
    private String data;
    private String path;

    public Data(String data, String path) {
        this.data = data;
        this.path = path;
    }

    public String getData() {
        return data;
    }

    public String getPath() {
        return path;
    }
}
